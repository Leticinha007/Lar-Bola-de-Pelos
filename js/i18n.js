/* ---------------------------------------------------------
   TRANSLATE ELEMENT — icon-aware text replacement
   --------------------------------------------------------- */
function translateEl(el, key) {
  const val = window.t(key);
  if (el.querySelector('i, svg')) {
    el.childNodes.forEach(n => {
      if (n.nodeType === Node.TEXT_NODE && n.textContent.trim()) n.textContent = ' ' + val;
    });
  } else {
    el.textContent = val;
  }
}

/* ---------------------------------------------------------
   APPLY LANG
   --------------------------------------------------------- */
function applyLang() {
  const lang    = window.getLang();
  const page    = getCurrentPage();
  const pageKey = getPageKey();

  /* --- nav + footer "Páginas" links --- */
  document.querySelectorAll('.nav-link, .f-col:first-child .f-links a').forEach(link => {
    const href = link.getAttribute('href');
    const base = href ? href.split('#')[0].split('/').pop() || 'index.html' : null;
    const key  = base ? navHrefKeyMap[base] : null;
    if (key) link.textContent = window.t(key);
  });

  /* --- page hero --- */
  const heroLabel = document.querySelector('.page-hero-label');
  if (heroLabel && pageKey) heroLabel.innerHTML = window.t(`hero.${pageKey}.label`);
  const heroTitle = document.querySelector('.page-hero-title');
  if (heroTitle && pageKey) heroTitle.innerHTML = window.t(`hero.${pageKey}.title`);
  const heroSub = document.querySelector('.page-hero-subtitle');
  if (heroSub && pageKey) heroSub.textContent = window.t(`hero.${pageKey}.subtitle`);
  const indexSub = document.querySelector('.hero-subtitle');
  if (indexSub) indexSub.textContent = window.t('hero.index.subtitle');

  /* --- filter buttons --- */
  document.querySelectorAll('.filter-btn').forEach(btn => {
    const f = btn.dataset.filter; if (!f) return;
    const val = window.t(`filter.${f}`);
    if (f === 'todos') {
      const icon = btn.querySelector('i');
      btn.innerHTML = '';
      if (icon) btn.appendChild(icon.cloneNode(true));
      btn.appendChild(document.createTextNode(' ' + val));
    } else { btn.textContent = val; }
  });

  /* --- navbar CTA --- */
  const navbarCta = document.querySelector('.navbar-cta');
  if (navbarCta) {
    const txt = window.t(ctaKeyMap[page] || 'nav.cta_adopt');
    navbarCta.childNodes.forEach(n => { if (n.nodeType === Node.TEXT_NODE) n.textContent = ' ' + txt; });
  }
  const mobileCta = document.querySelector('.mobile-menu-cta');
  if (mobileCta) {
    const txt = window.t(mobilCtaKeyMap[page] || 'nav.cta_adopt');
    mobileCta.childNodes.forEach(n => { if (n.nodeType === Node.TEXT_NODE) n.textContent = ' ' + txt; });
  }

  /* --- [data-i18n] --- */
  document.querySelectorAll('[data-i18n]').forEach(el => translateEl(el, el.dataset.i18n));

  /* --- [data-i18n-html] (innerHTML) --- */
  document.querySelectorAll('[data-i18n-html]').forEach(el => { el.innerHTML = window.t(el.dataset.i18nHtml); });

  /* --- [data-i18n-placeholder] --- */
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = window.t(el.dataset.i18nPlaceholder); });

  /* --- footer column headings --- */
  const fCols = document.querySelectorAll('.f-col');
  if (fCols[0]?.querySelector('h5')) fCols[0].querySelector('h5').textContent = window.t('footer.pages');
  if (fCols[1]?.querySelector('h5')) fCols[1].querySelector('h5').textContent = window.t('footer.how_help');
  if (fCols[2]?.querySelector('h5')) fCols[2].querySelector('h5').textContent = window.t('footer.contact_title');

  /* --- footer "Como Ajudar" links --- */
  const helpKeys = ['footer.help.adopt','footer.help.sponsor','footer.help.donate','footer.help.items','footer.help.volunteer'];
  if (fCols[1]) fCols[1].querySelectorAll('.f-links a').forEach((a, i) => { if (helpKeys[i]) a.textContent = window.t(helpKeys[i]); });

  /* --- footer bottom --- */
  const fBottomP = document.querySelector('.footer-bottom p');
  if (fBottomP) {
    const nodes = [...fBottomP.childNodes].reverse();
    const lastText = nodes.find(n => n.nodeType === Node.TEXT_NODE && n.textContent.trim());
    if (lastText) lastText.textContent = '. ' + window.t('footer.rights');
  }
  const fBottomLinks = document.querySelectorAll('.f-bottom-links a');
  const blKeys = ['footer.privacy','footer.terms','nav.contact'];
  fBottomLinks.forEach((a, i) => { if (blKeys[i]) a.textContent = window.t(blKeys[i]); });

  /* --- footer brand description --- */
  const fDesc = document.querySelector('.f-desc');
  if (fDesc) fDesc.textContent = window.t('footer.desc');

  /* --- footer PIX label --- */
  const fPixP = document.querySelector('.f-pix p');
  if (fPixP) { fPixP.innerHTML = `<i class="fa-solid fa-qrcode"></i> ${window.t('footer.pix')}`; }

  /* --- PIX copy hint --- */
  document.querySelectorAll('.pix-key-hint').forEach(el => {
    el.innerHTML = `<i class="fa-regular fa-copy"></i> ${window.t('common.pix_copy')}`;
  });
  document.querySelectorAll('.pix-big-note').forEach(el => {
    el.innerHTML = `<i class="fa-regular fa-copy"></i> ${window.t('common.pix_copy')}`;
  });

  /* --- lang selector UI --- */
  const lc = document.getElementById('langCurrent');
  if (lc) lc.textContent = langLabels[lang] || langLabels.pt;
  document.querySelectorAll('.lang-option').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
  document.querySelectorAll('.mobile-lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));

  /* --- theme tooltip --- */
  document.getElementById('themeToggle')?.setAttribute('aria-label', window.t('theme.tooltip'));
  const mtb = document.getElementById('mobileThemeToggle');
  if (mtb) {
    const icon = mtb.querySelector('i');
    mtb.innerHTML = '';
    if (icon) mtb.appendChild(icon.cloneNode(true));
    mtb.appendChild(document.createTextNode(' ' + window.t('theme.mobile')));
  }

  /* --- html lang attribute --- */
  const htmlLangMap = { pt:'pt-BR', en:'en', es:'es', it:'it', fr:'fr' };
  document.documentElement.lang = htmlLangMap[lang] || 'pt-BR';
}

/* ---------------------------------------------------------
   DARK MODE
   --------------------------------------------------------- */
const THEME_KEY = 'lbp_theme';

function initDarkMode() {
  applyTheme(localStorage.getItem(THEME_KEY) || 'light');
  const toggle  = document.getElementById('themeToggle');
  const mToggle = document.getElementById('mobileThemeToggle');
  const doToggle = () => {
    const next = document.documentElement.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
    localStorage.setItem(THEME_KEY, next);
    applyTheme(next);
  };
  toggle?.addEventListener('click', doToggle);
  mToggle?.addEventListener('click', doToggle);
}

function applyTheme(theme) {
  if (theme === 'dark') document.documentElement.setAttribute('data-theme', 'dark');
  else document.documentElement.removeAttribute('data-theme');
  updateThemeIcons(theme);
}

function updateThemeIcons(theme) {
  const isDark = theme === 'dark';
  const di = document.getElementById('themeIcon');
  const mi = document.getElementById('mobileThemeIcon');
  if (di) di.className = isDark ? 'fa-solid fa-sun' : 'fa-solid fa-moon';
  if (mi) mi.className = isDark ? 'fa-solid fa-sun' : 'fa-solid fa-moon';
}


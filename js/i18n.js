const LANG_KEY = 'lbp_lang';
const SUPPORTED_LANGS = ['pt', 'en', 'es', 'it', 'fr'];

window.getLang = () => {
  const s = localStorage.getItem(LANG_KEY);
  return SUPPORTED_LANGS.includes(s) ? s : 'pt';
};
window.setLang = lang => {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  localStorage.setItem(LANG_KEY, lang);
  applyLang();
};
window.t = key => {
  const lang = window.getLang();
  return (translations[lang]?.[key]) ?? (translations.pt?.[key]) ?? key;
};

/* ---------------------------------------------------------
   PAGE DETECTION
   --------------------------------------------------------- */
function getCurrentPage() {
  const p = location.pathname.split('/').pop() || 'index.html';
  return p === '' ? 'index.html' : p;
}
function getPageKey() {
  return { 'index.html':'index','adocao.html':'adoption','apadrinhamento.html':'sponsorship','doacao.html':'donation','contato.html':'contact' }[getCurrentPage()] || null;
}

